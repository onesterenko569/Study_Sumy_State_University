#include<stdio.h> // підключення бібліотеки
int main() // головна функція програми
{
	int i, sum = 0, N; // задання змінних
	printf("Enter N\n"); // виведення тексту
	scanf_s("%d", &N); // надання можливості вписати значення N
	for (int i = 1; i <= N; i++) // введення циклу
	{
		sum += +i; // формула для sum
	}
	printf("Sum from 1 to %d = %d\n", N, sum); // виведення результату обчислення 
	return 0; // завершення програми
}
